def sell_product(products):
  decision = input("Please enter products to sell: ")
  decision = decision.lower().split("-")
  for rwanda in decision:
    rwanda = rwanda.split("_")
    if rwanda[0] in products:
      if int(rwanda[1]) > int(products[rwanda[0]]) :
        print("There is not enough",rwanda[0],"in inventory.")
      elif int(rwanda[1]) == int(products[rwanda[0]]):
        products.pop(rwanda[0])
        print(rwanda[1],rwanda[0], "sold.")

      elif int(rwanda[1]) < int(products[rwanda[0]]) :
        print(rwanda[1],rwanda[0], "sold.")
        products[rwanda[0]] -= int(rwanda[1])

    else:
      print(rwanda[0],"does not exist in inventory.")

