def import_inventory():
  f = open("products.txt", encoding="latin-1")
  d = {}
  for line in f:
    line = line.lower().strip().split("-")
    for kalas in line:
      kalas=kalas.split("_")
      if kalas[0] not in d :  
        d[kalas[0]] = int(kalas[1])
      else:
        d[kalas[0]]+= int(kalas[1]) 
  f.close()
  kasa = {}
  kedi = sorted(d.keys())

  for makine in kedi:
    kasa[makine] = int(d[makine])
  return kasa
